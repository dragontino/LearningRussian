    private fun uploadWords(taskType: TaskType) {
        if (!taskRepository.isTableEmpty(taskType))
            return

        getWords(taskType).forEach {
            val pos = if (taskType == TaskType.Paronym) 0
            else findUpper(it)

            if (pos < 0) return

            val task = when (taskType) {
                TaskType.Assent -> Assent(it.lowercase(), pos)
                // TODO: 08.04.2022 сделать альтернативные буквы
                TaskType.Suffix -> Suffix(it.lowercase(), pos, "")
                TaskType.Paronym -> Paronym(it.split(":").toTypedArray())
                TaskType.SteadyExpression -> SteadyExpression(it)
            }

            taskRepository.addTask(task)
        }
    }


    // возвращает индекс первой заглавной буквы в слове
    private fun findUpper(word: String) = word.indexOfFirst {
        it.isUpperCase()
    }

    // возвращает массив слов для данного типа
    private fun getWords(taskType: TaskType) = when (taskType) {
        TaskType.Assent -> assentWords
        TaskType.Suffix -> suffixWords
        TaskType.Paronym -> paronymWords
        TaskType.SteadyExpression -> expressionWords
    }


    fun getWordsCount(taskType: TaskType) =
        getWords(taskType).size



    // возвращает рандомное слово из бд
    fun getRandomWord(taskType: TaskType, @ColorInt highlightColor: Int): Spannable {
        val randomNumber = random.nextInt(getWordsCount(taskType))

        return if (taskType == TaskType.Paronym) {
            val paronym = getTask(taskType, randomNumber).toUpperCase() as Paronym
            paronym.paint(highlightColor)
        } else
            getWord(taskType, randomNumber, highlightColor)
    }


    // возвращает рандомное слово и к нему несколько альтернативных вариантов (для QuizFragment)
    // возвращает пару, первое слово в которой - правильный ответ
    // TODO: 10.04.2022 убрать методы setOfAssents
    fun getRandomArrayWords(taskType: TaskType, @ColorInt highlightColor: Int):
            Pair<Spanned, ArrayList<Spanned>>? {

        var index = random.nextInt(getWordsCount(taskType))

        return when (taskType) {
            TaskType.Assent -> {
                var setAssents: Pair<Spanned, ArrayList<Spanned>>?

                do {
                    setAssents = getSetOfAssents(index, highlightColor)
                    if (setAssents != null) break
                    index = random.nextInt(getWordsCount(taskType))
                } while (true)

                setAssents
            }
            TaskType.Suffix -> null
            TaskType.Paronym -> {
                val paronym = getTask(taskType, index) as Paronym

                val phrase = paronym.removeWordFromPhrase(highlightColor)
                val variants: ArrayList<Spanned> = arrayListOf(SpannableString(paronym.word))

                paronym.getRandomAlternatives(3).forEach {
                    variants += SpannableString(it)
                }

                Pair(phrase, variants)
            }
            TaskType.SteadyExpression -> null
        }
    }



    // возвращает слово из бд по позиции (прописная буква закрашена)
    fun getWord(taskType: TaskType, position: Int, @ColorInt highlightColor: Int): SpannableString {
        val task = getTask(taskType, position).toUpperCase()

        return when (taskType) {
            TaskType.Paronym, TaskType.SteadyExpression -> SpannableString(task.word)
            else -> task.word.paint(task.position, highlightColor)
        }
    }


