    //возвращает слово с буквой на заданной позиции, закрашенной в заданный цвет
    private fun String.paint(position: Int, @ColorInt highlightColor: Int): SpannableString {

        if (position < 0) {
            Log.e(TAG, "Нет прописной буквы!")
            return SpannableString(this)
        }

        return SpannableString(this).apply {
            setSpan(
                ForegroundColorSpan(highlightColor),
                position,
                position + 1,
                SpannableString.SPAN_EXCLUSIVE_EXCLUSIVE
            )
        }
    }


    fun paintParonym(paronym: Paronym, @ColorInt highlightColor: Int, position: Int? = null) =
        paronym.paint(highlightColor, position)


    // Закрашивает слово в фразе
    private fun Paronym.paint(@ColorInt highlightColor: Int, variantPosition: Int? = null): Spannable {

        val phraseArray =
            (if (variantPosition == null) randomVariant
            else getVariant(variantPosition)).split(" ")

        val position = phraseArray.indexOfFirst { it == word }

        return phraseArray.draw(highlightColor, position, position + 1)
    }


