    // возвращает фразу, у которой вместо слова стоит многоточие
    private fun Paronym.removeWordFromPhrase(highlightColor: Int): SpannedString {
        val phrase = randomVariant.split(" ")

        return buildSpannedString {
            phrase.forEach {
                if (it == word) {
                    append(
                        "...",
                        ForegroundColorSpan(highlightColor),
                        Spannable.SPAN_EXCLUSIVE_EXCLUSIVE
                    )
                    append(" ")
                } else append(it, " ")
            }
        }
    }



    private fun getSetOfAssents(index: Int, @ColorInt highlightColor: Int):
            Pair<Spanned, ArrayList<Spanned>>? {

        val assents = taskRepository.getNonCheckedAssents()

        val assent = if (assents.isEmpty()) {
            for (id in 0 until getWordsCount(TaskType.Assent))
                taskRepository.updateAssent(id, false)

            getTask(TaskType.Assent, index) as Assent
        } else
            assents[index % assents.size]

        assent.removeExtraWords()
        val indexes = getVowelIndexes(assent.word, assent.position) ?: return null

        taskRepository.updateAssent(index, true)

        val rightWord = assent.word.paint(indexes.first, highlightColor)
        val variants = ArrayList<Spanned>()

        indexes.second.forEach {
            variants += assent.word.paint(it, highlightColor)
        }

        return Pair(rightWord, variants)
    }


    // на позиции position ставится прописная буква
    private fun Task.toUpperCase(): Task {
        if (position < 0 || position >= word.length) return this

        word = word.edit(position) { it.uppercaseChar() }
        return this
    }


    // удаляет в словосочетании слова без прописных букв
    private fun Task.removeExtraWords(): Task {
        val text = word.split(" ")

        if ("отзыв" in text || text.size == 1)
            return this

        var len = 0
        text.forEach {
            if (len + it.length > position) {
                word = it
                position -= len
                return@forEach
            }
            len += it.length + 1
        }

        return this
    }


    // тоже самое, что и выше, но без объекта класса task
    private fun String.removeExtraWords(): String {
        val pos = findUpper(this)
        if (pos < 0) return this

        return Task(this, pos).removeExtraWords().word
    }

    /**
     * возвращает словарь, ключём которого является индекс правильной буквы,
     * а значением – массив, содержащий индексы гласных букв в слове
     * @param indexOfRightLetter — индекс правильной буквы
     */
    private fun getVowelIndexes(word: String, indexOfRightLetter: Int): Pair<Int, List<Int>>? {
        val indexes = ArrayList<Int>()

        if ("отзыв" in word)
            return Pair(0, arrayListOf(3))

        for (elem in word.indices) {
            if (word.substring(elem, elem + 1).matches(Regex("^(?ui:[аеёиоуыэюя]).*")) &&
                elem != indexOfRightLetter
            )
                indexes += elem
        }

        indexes.shuffle()

        val result: Pair<Int, List<Int>> = Pair(indexOfRightLetter, indexes)

        if (indexes.size < 2)
            return null
        else if (indexes.size > 4)
            result[indexOfRightLetter] = indexes.subList(0, 4)

        return result
    }




